    @SuppressWarnings("StatementWithEmptyBody")
    @Override
    public boolean onNavigationItemSelected(MenuItem item)
    {
        // Handle navigation view item clicks here.
        int id = item.getItemId();

        if (id == R.id.nav_cart)
        {
            Intent intent=new Intent(HomeActivity.this,Cart.class);
            startActivity(intent);
        }
        else if (id == R.id.nav_order)
        {
            Intent intent = new Intent(HomeActivity.this,Orders.class);
          //  intent.putExtra("mobileno", mob);
            startActivity(intent);
        }
        else if (id == R.id.nav_category)
        {
            Intent intent= new Intent(HomeActivity.this,categoryselect.class);
            startActivity(intent);
        }
        else if(id==R.id.contact){
            Intent intent = new Intent(HomeActivity.this, Contactme.class);
            startActivity(intent);
        }
        else if (id == R.id.nav_setting)
        {
            Intent intent = new Intent(HomeActivity.this, SettingsActivity.class);
            startActivity(intent);
        }
        else if (id == R.id.nav_logout)
        {
            Paper.book().destroy();

            Intent intent = new Intent(HomeActivity.this, MainActivity.class);
            intent.addFlags(Intent.FLAG_ACTIVITY_NEW_TASK | Intent.FLAG_ACTIVITY_CLEAR_TASK);
            startActivity(intent);
            finish();
        }

        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        drawer.closeDrawer(GravityCompat.START);
        return true;
    }
